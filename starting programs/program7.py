num1=int(input("enter first num"))
num2=int(input("enter the second num"))
num3=input("enter the operator +,-,*,/")
if num3=="+":
    sum=num1+num2
    print(sum)
elif num3=="-":
    sum=num1-num2
    print(sum)
elif  num3=="*":
    sum=num1*num2
    print(sum)
elif num3=="/":
    sum=num1*num2
    print(sum)

else:
    print("invalid")
    
    